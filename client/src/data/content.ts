export type StepMotionKey =
  | "limb-rotate"
  | "torso-fold"
  | "hip-shift"
  | "arm-extend"
  | "leg-extend"
  | "inhale"
  | "exhale"
  | "lift"
  | "twist"
  | "ground"
  | "balance"
  | "settle";

export type Step = {
  text: string;
  pose?: string; // optional per-step SVG key
  stepMotion?: StepMotionKey; // animated mini-clip primitive
  // Normalized (0-1) coordinates marking the body region this step targets,
  // used by DemoMode to draw an animated "focus halo" over the pose image.
  focusZone?: { cx: number; cy: number; r: number; label: string };
};

export type Severity = "avoid" | "modify" | "caution";
export type AvoidRow = { condition: string; severity: Severity };

export type Variation = {
  description: string;
  props: string[];
  cues: string[];
  holdSeconds: number;
};

export type Variations = {
  beginner: Variation;
  intermediate: Variation;
  advanced: Variation;
};

export type Asana = {
  slug: string;
  sanskrit: string;
  english: string;
  category: Category;
  difficulty: Difficulty;
  hold: string; // hold duration label
  holdSeconds: number; // used by the timer
  pose: string; // PoseSvg key
  summary: string;
  breathing: string;
  benefits: string[];
  contraindications: string[]; // legacy flat list (kept for back-compat)
  avoidIf: AvoidRow[]; // structured "who should avoid"
  modifications: string;
  steps: Step[];
  variations: Variations;
  // Body regions the pose stretches — surfaced in the "You'll feel this in..."
  // card on the detail page.
  stretchZones: StretchZone[];
};

export type StretchZone = {
  region: string; // e.g. "Hamstrings"
  sensation: string; // 1-line description of the sensation
  intensity: "low" | "medium" | "strong";
  primary: boolean; // primary vs secondary stretch
};

